export const REGEX = {
    name: /^[a-zA-Z ]*$/,
    email: /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
    onlyNumber: /^\d+$/,
    cardExpiry: /^(0[1-9]{1}|10|11|12)\/(20[0-9]{2})$/,
    cardNumber: /^\d{12,20}$/,
    contactNumber: /^\d{1,14}$/,
    cvvOrCvc: /^[0-9]{3,4}$/
}